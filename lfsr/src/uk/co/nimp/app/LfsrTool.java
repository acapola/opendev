        msg+="If a reference ARG_LFSR is given and a reference sequence as well:\n";
        msg+=tab+"- The reference ARG_LFSR is compared with the one computed from the sequence.\n";
        msg+=tab+"- The reference sequence is search inside the sequence generated by the reference ARG_LFSR\n";
        msg+="\n"+credits;
